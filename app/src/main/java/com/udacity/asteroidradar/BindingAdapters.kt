@BindingAdapter("codename")
fun bindTextViewToNamecode(textView: TextView, asteroid: Asteroid?) {
    textView.text = asteroid?.codename
}

@BindingAdapter("closeApproachDate")
fun bindTextViewToDate(textView: TextView, asteroid: Asteroid) {
    textView.text = asteroid.closeApproachDate
}

// Bind PictureOfDay with Image Caching Picasso
@BindingAdapter("bindPictureOfDay")
fun bindPictureOfDay(imgView: ImageView, imgUrl: String?) {
    imgUrl?.let {
        Picasso.with(imgView.context)
            .load(imgUrl).placeholder(R.drawable.placeholder_picture_of_day)
            .error(R.drawable.ic_help_circle)
            .into(imgView);
    }
}
